import {PropsWithChildren, useState} from 'react';
import {StyleSheet, TouchableOpacity} from 'react-native';

import {AppText} from '@/components/theme/app-text';
import {ViewContainer} from '@/components/theme/view-container';
import {IconSymbol} from '@/components/icon-symbol';
import {Colors} from '@/constants/theme';
import {useColorScheme} from '@/hooks/use-color-scheme';

export function Collapsible({children, title}: PropsWithChildren & { title: string }) {
	const [isOpen, setIsOpen] = useState(false);
	const theme = useColorScheme() ?? 'light';

	return (
		<ViewContainer>
			<TouchableOpacity
				style={styles.heading}
				onPress={() => setIsOpen(value => !value)}
				activeOpacity={0.8}>
				<IconSymbol
					name="chevron.right"
					size={18}
					weight="medium"
					color={theme === 'light' ? Colors.light.icon : Colors.dark.icon}
					style={{transform: [{rotate: isOpen ? '90deg' : '0deg'}]}}
				/>

				<AppText type="defaultSemiBold">{title}</AppText>
			</TouchableOpacity>
			{isOpen && <ViewContainer style={styles.content}>{children}</ViewContainer>}
		</ViewContainer>
	);
}

const styles = StyleSheet.create({
	heading: {
		flexDirection: 'row',
		alignItems: 'center',
		gap: 6,
	},
	content: {
		marginTop: 6,
		marginLeft: 24,
	},
});
